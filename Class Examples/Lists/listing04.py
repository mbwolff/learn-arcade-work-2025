# Copy of the list to sum
my_list = [5, 76, 8, 5, 3, 3, 56, 5, 23]

# Initial sum should be zero
list_total = 0

# Loop through array, copying each item in the array into
# the variable named item.
for item in my_list: # for i in range(10)
    # Add each item
    list_total += item

# Print the result
print(list_total)
